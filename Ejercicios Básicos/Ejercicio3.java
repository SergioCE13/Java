public class Ejercicio3 {
    public static void main(String[] args) {
        System.out.println("Contando del 1 al 15:");
        for (int i = 1; i <= 15; i++) {
            System.out.println(i);
        }

        System.out.println("Contando de 2 en 2 reversa:");
        for (int i = 15; i >= 1; i -= 2) {
            System.out.println(i);
        }
    }
}